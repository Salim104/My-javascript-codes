//////  Ternary Operator

/*
The ternary operator in JavaScript is a concise way to write conditional 
expressions. It's often used as a shorthand for if-else statements when assigning values 
or deciding outcomes based on a condition. It consists of three parts:

condition ? expression_if_true : expression_if_false;

// code sample

let age = 18;
let access = age >= 18 ? "Allowed" : "Denied";
console.log(access); // Output: "Allowed"

*/

